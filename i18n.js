const HTML_LANG = { en:'en', 'zh-CN':'zh-Hans', 'zh-TW':'zh-Hant', ja:'ja' };
let LANG = localStorage.getItem('ssm_lang');
if(!I18N[LANG]) LANG='en';

function t(key, params){
  let s = (I18N[LANG] && I18N[LANG][key] != null) ? I18N[LANG][key]
        : (I18N.en[key] != null ? I18N.en[key] : key);
  if(params) for(const k in params) s = s.split('{'+k+'}').join(params[k]);
  return s;
}
function applyI18n(root){
  root = root || document;
  root.querySelectorAll('[data-i18n]').forEach(el=>{ el.textContent = t(el.getAttribute('data-i18n')); });
  root.querySelectorAll('[data-i18n-html]').forEach(el=>{ el.innerHTML = t(el.getAttribute('data-i18n-html')); });
  root.querySelectorAll('[data-i18n-title]').forEach(el=>{ el.title = t(el.getAttribute('data-i18n-title')); });
  document.documentElement.lang = HTML_LANG[LANG] || 'en';
}
function setLang(lang){
  if(!I18N[lang]) lang='en';
  LANG=lang; localStorage.setItem('ssm_lang', lang);
  applyI18n();
  if(typeof window.onLangChange === 'function') window.onLangChange();
}
function currentLang(){ return LANG; }
// apply static strings as early as possible
applyI18n();
